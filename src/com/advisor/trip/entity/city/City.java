package com.advisor.trip.entity.city;

/**
 * @author 董晋坤
 *	城市数据库的字段
 */
public class City {
	
	private int id;
	private String name;
	private int number;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getNumber() {
		return number;
	}
	public void setNumber(int number) {
		this.number = number;
	}
	
}
